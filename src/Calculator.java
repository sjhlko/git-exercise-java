public class Calculator {
    public void plus(int a, int b){
        System.out.println(a+b);
    }
    public void minus(int a, int b){
        System.out.println(a-b);
    }
    public void multiple(int a, int b){
        System.out.println(a*b);
    }
    public void devide(int a, int b){
        System.out.println(a/b);
    }

}
